// --- Types ---

export type Season = 'spring' | 'summer' | 'autumn' | 'winter' | 'none';

export type SemanticCluster =
  | 'water'
  | 'stone'
  | 'sky'
  | 'growth'
  | 'stillness'
  | 'time'
  | 'light'
  | 'sound'
  | 'absence';

export type PivotType =
  | 'scale-shift'
  | 'time-shift'
  | 'absence'
  | 'observer'
  | 'question'
  | 'acceptance';

export type Setting = 'urban' | 'natural' | 'domestic';

export type NounStrength = 'strong' | 'weak';

export type NounEntry = {
  readonly word: string;
  readonly strength: NounStrength;
};

// Noun tagging rules:
//   strong: never repeat anywhere in the haiku (ramen, crow, jizo, fox, spider, tatami)
//   weak:   never repeat on adjacent lines, but may echo on Lines 1↔3 (rain, stone, light)
//   I/me/my: always tag ALL THREE as strong on any fragment containing one of them
//
// Setting rules:
//   urban:    streets, traffic, neon, trains, scooters, buildings, shops
//   natural:  weather, plants, animals, landscape, water, sky
//   domestic: kitchen, tea, rooms, furniture, food, personal objects
//   Rule: engine rejects combinations where all 3 lines share the same setting
//
// Conceptual group rules:
//   Optional tag linking fragments about the same thing without sharing a literal noun.
//   e.g. 'tea' group links 'cold tea sits half-full' with 'the kettle sits cold'
//   Treated as a strong noun for adjacency filtering.
//   Only tag where obvious overlap exists. Most fragments won't have one.

export type Line1Fragment = {
  readonly text: string;
  readonly syllables: 5;
  readonly season: Season;
  readonly clusters: readonly SemanticCluster[];
  readonly setting: Setting;
  readonly nouns: readonly NounEntry[];
  readonly conceptual_group?: string;
};

export type Line2Fragment = {
  readonly text: string;
  readonly syllables: 7;
  readonly season: Season;
  readonly clusters: readonly SemanticCluster[];
  readonly setting: Setting;
  readonly nouns: readonly NounEntry[];
  readonly conceptual_group?: string;
};

export type Line3Fragment = {
  readonly text: string;
  readonly syllables: 5;
  readonly season: Season;
  readonly clusters: readonly SemanticCluster[];
  readonly pivot: PivotType;
  readonly setting: Setting;
  readonly nouns: readonly NounEntry[];
  readonly conceptual_group?: string;
};

